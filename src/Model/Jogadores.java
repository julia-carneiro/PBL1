package Model;

public class Jogadores{
	
	private String name;
	private String codJog;
	private int cards;
	private int qtdGols;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public String getCodJog() {
		return codJog;
	}
	public void setCodJog(String codJog) {
		this.codJog = codJog;
	}
	
	public int getCards() {
		return cards;
	}
	public void setCards(int cards) {
		this.cards = cards;
	}
	
	public int getQtdGols() {
		return qtdGols;
	}
	public void setQtdGols(int qtdGols) {
		this.qtdGols = qtdGols;
	}
	
}

